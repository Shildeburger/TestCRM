<script setup>
import { useForm, router } from "@inertiajs/vue3";
import ContactForm from "./Partials/ContactForm.vue";
import AuthLayout from "../../Layouts/AuthLayout.vue";

const form = useForm({
    //useForm - это функция, которая позволяет нам создавать реактивный объект формы,
    //  который будет использоваться для связывания данных формы с полями ввода в шаблоне.
    //  Она также предоставляет методы для отправки данных на сервер и обработки ошибок валидации.
    name: "", //name - это поле формы, которое будет использоваться для ввода имени контакта. Изначально оно пустое.
    // Когда пользователь вводит данные в это поле, значение form.name будет автоматически обновляться благодаря директиве
    //  v-model в шаблоне.
    // Это позволяет нам легко получать доступ к данным формы и отправлять их на сервер при необходимости.
    email: "",
    phone: "",
    company: "",
    job_title: "",
    country: "",
    notes: "",
    is_favorite: false,
});

function submit() {
    //Функция подтверждения сохранения
    form.post(route("contacts.store"), {
        // form - это объект формы, который мы создали с помощью useForm.
        //  Он содержит все данные формы и методы для отправки данных на сервер.
        // post - это метод объекта формы, который используется для отправки данных на сервер с помощью HTTP POST запроса.
        //  Он принимает два аргумента: URL, на который будет отправлен запрос, и объект с дополнительными параметрами,
        //  такими как обработчики событий.
        // POST-запрос - это тип HTTP-запроса, который обычно используется для создания новых ресурсов на сервере.
        //  В данном случае, мы используем form.post для отправки данных формы на сервер, чтобы создать новый контакт.
        //В чем отличие POST от GET? - GET-запросы обычно используются для получения данных с сервера,
        //  в то время как POST-запросы используются для отправки данных на сервер, например, при создании или обновлении ресурса.
        // route("contacts.store") - это функция, которая генерирует URL для маршрута "contacts.store".
        //  Этот маршрут должен быть определен в вашем Laravel приложении и обычно используется для создания нового контакта.
        //  Когда мы вызываем form.post(route("contacts.store")), мы отправляем данные формы на сервер, и если запрос успешен,
        //  мы можем выполнить дополнительные действия, указанные в объекте параметров.
        //  В данном случае, мы указываем onSuccess, который будет вызван после успешного ответа от сервера.
        onSuccess: () => form.reset("notes"), // onSuccess - это обработчик события, который будет вызван после успешного ответа
        //  от сервера на POST запрос.
        //  В данном случае, мы передаем функцию, которая вызывает form.reset("notes").
        //  Метод reset объекта формы используется для сброса значений полей формы.
        //  В данном случае, мы передаем "notes" в качестве аргумента, что означает, что только поле "notes" будет сброшено
        //  до своего начального значения (пустой строки).
        //Зачем сбрасывать notes? - Это может быть полезно, если вы хотите очистить поле заметок после успешного создания контакта,
        //  но оставить остальные поля формы заполненными для удобства пользователя при создании нескольких контактов подряд.
    });
}

function cancel() {
    //Функция отмены создания контакта
    router.visit(route("contacts.index")); // router.visit - это метод, который позволяет нам программно навигировать пользователя
    //  на другой маршрут в нашем приложении.
    //  В данном случае, мы используем router.visit(route("contacts.index")),
    //  чтобы перенаправить пользователя на маршрут "contacts.index", который обычно отображает список контактов.
    //  Это позволяет пользователю отменить процесс создания нового контакта и вернуться к списку контактов
    //  без сохранения изменений.
}
</script>

<template>
    <AuthLayout>
        <div class="p-6">
            <h1 class="text-xl font-bold mb-4">Create contact</h1>

            <ContactForm
                :form="form"
                :errors="form.errors"
                @submit="submit"
                @cancel="cancel"
            />
        </div>
    </AuthLayout>
</template>
